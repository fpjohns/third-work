//Ниже напиши на свой выбор любой цикл for или while, который выведет все элементы в консоль.
let arr = ['red', 'blue', 'black', 'white', 'orange', 'most', 'play', 'game', 'yes', 'not']
for(i = 0; i < arr.length; i++){
    console.log(arr[i])
}
let i = 0
while(i < arr.length){
    console.log(arr[i])
    i++
}